package com.lambton;

public class CollegeSystem
{
    public static void main(String[] args)
    {
        System.out.println("Hello, Java");

        int sum = 0;

        for(int i= 1; i <= 10; i++)
        {
            sum = sum + i;
        }

        System.out.println("SUM of 1 to 10 is : " + sum);
    }
}
